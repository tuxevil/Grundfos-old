using System;
using System.Collections.Generic;
using System.Text;

namespace PartnerNet.Domain
{
    public class PurchaseOrderItemInformation
    {
        private int id;
        private string productName;
        private double quantity;
        private int quantitySuggested;
        private double price;
        private string currency;
        private double totalPrice;
        private int stock;
        private string productCode;
        private int provideID;
        private int status;
        


        public virtual int Id
        {
            get { return id; }
            set { id = value; }
        }

        public virtual string ProductName
        {
            get { return productName; }
            set { productName = value; }
        }

        public virtual double Quantity
        {
            get { return quantity; }
            set { quantity = value; }
        }

        public virtual double Price
        {
            get { return price; }
            set { price = value; }
        }

        public virtual double TotalPrice
        {
            get { return totalPrice; }
            set { totalPrice = value; }
        }

        public virtual int Stock
        {
            get { return stock; }
            set { stock = value; }
        }

        public virtual string ProductCode
        {
            get { return productCode; }
            set { productCode = value; }
        }

        public virtual int ProvideID
        {
            get { return provideID; }
            set { provideID = value; }
        }

        public virtual int Status
        {
            get { return status; }
            set { status = value; }
        }

        public virtual int QuantitySuggested
        {
            get { return quantitySuggested; }
            set { quantitySuggested = value; }
        }

        public virtual string Currency
        {
            get { return currency; }
            set { currency = value; }
        }

       
    }
}
